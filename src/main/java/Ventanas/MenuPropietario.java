    private void btnFacturaMensualMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnFacturaMensualMouseClicked
        Object[] opcionesTerminos = {"Descargar", "Cancelar"};
        int seleccionTerminos = JOptionPane.showOptionDialog(null, "¬øDeseas descargar tu factura mensual?", "Aceptar/Rechazar",
            JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null, opcionesTerminos, opcionesTerminos[0]);
        if (seleccionTerminos == JOptionPane.YES_OPTION) {
            GestionFactura gf = new GestionFactura();
            gf.descargarFacturaMensual(this.idPropietario);
        } else if (seleccionTerminos == JOptionPane.NO_OPTION) {
            return;
        }
    }//GEN-LAST:event_btnFacturaMensualMouseClicked

