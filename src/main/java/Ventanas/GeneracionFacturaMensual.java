    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        this.numFactura = jTextFieldNumFactura.getText();
        this.fechaPago = jTextFieldFechaPago.getText();
        this.fechaFactura = LocalDate.now().format(this.f);
        this.idEmisor = jTextFieldIdEmisor.getText();
        this.costo = jTextFieldCosto.getText();
        
        if (this.numFactura.isEmpty() || this.fechaPago.isEmpty() || this.idEmisor.isEmpty() || this.costo.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debes llenar todos los campos obligatorios.");
            return;
        }

        VerificacionDatos vd = new VerificacionDatos();
        if (!vd.verificarFecha(jTextFieldFechaPago, "dd/MM/yyyy")) {
            JOptionPane.showMessageDialog(null, "La fecha de pago debe estar en formato dd/MM/yyyy.");
            return;
        }

        try {
            double costoDouble = Double.parseDouble(this.costo);
            if (costoDouble < 0) {
                JOptionPane.showMessageDialog(null, "El costo debe ser un valor positivo.");
                return;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "El costo debe ser un n√∫mero v√°lido.");
            return;
        }
        // Verificar si la factura ya existe en el archivo
        String archivoCSV = "BaseDatos/facturas.csv";
        boolean facturaExiste = false;

        try (BufferedReader reader = new BufferedReader(new FileReader(archivoCSV))) {
            String linea;
            while ((linea = reader.readLine()) != null) {
                String[] datos = linea.split(";");
                if (datos.length > 0 && datos[0].equals(this.numFactura)) {
                    facturaExiste = true;
                    break;
                }
            }
        } catch (IOException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error al leer el archivo de facturas.");
            return;
        }

        
        if (facturaExiste) { // Si la factura ya existe, mostramos un mensaje y no la creamos
            JOptionPane.showMessageDialog(null, "El n√∫mero de factura " + this.numFactura + " ya existe. No se puede crear la factura.");
            return;
        }
        
        try (BufferedWriter writer = new BufferedWriter(new FileWriter(archivoCSV, true))) {// Si la factura no existe, procedemos a guardarla en el archivo
            writer.write(this.numFactura + ";" +
                         this.fechaPago + ";" +
                         this.fechaFactura + ";" +
                         this.costo + ";" +
                         "0" + ";" +  // inicialmente el valor pagado es 0
                         this.idEmisor);
            writer.newLine();
            writer.flush();

            JOptionPane.showMessageDialog(null, "Factura creada y guardada correctamente en el archivo CSV.");
        } catch (IOException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error al guardar la factura en el archivo CSV.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

